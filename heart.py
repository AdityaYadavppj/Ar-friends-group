import turtle as t
t.color('red')
t.speed(6)
t.begin_fill()
t.fillcolor('red')
t.left(140)
t.forward(180)
t.circle(-90,200)
t.setheading(60)
t.circle(-90,200)
t.forward(180)
t.end_fill()
t.color('white')
t.rt(180)
t.penup()
t.fd(200)
t.lt(120)
t.fd(60)
t.rt(90)
t.lt(115)
t.fd(150)
t.pendown()
t.write('Aditya')
t.hideturtle()

t.penup
t.done()